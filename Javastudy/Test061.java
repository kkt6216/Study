// ○ 다중 for문(반복문 중첩)을 활용하기 별 찍기 실습

// ○ 과제
//	  다음과 같은 내용이 출력될 수 있도록
//	  반복문의 중첩 구문을 작성하여 프로그램을 구현한다.

// 실행 예)
/*
	*
   ***
  *****
 *******
*********
*/
public class Test061
{
	public static void main(String[] args) 
	{

		int i;
		int j;
		int k;


		for (i=1;i<=5 ;i++ )
		{
			
			for (j=5; j>i; j--)
			{
				System.out.print(" ");
			}
			
				for (k=1; k<=2*i-1; k++)
				{
						
					System.out.print('*');
				}
				System.out.println();
		}

	}
}